s = input().split()


def proverka(matrix):
    flag = True
    for b in matrix:
        if "." in b:
            flag = False
            break
    return flag


n, m = int(s[0]), int(s[1])
lst = []
for i in range(n):
    lst.append([])
    for j in range(m):
        lst[i].append(".")
lst[0][0] = "1"
stroka = 0
ryad = 0
count = 1
flag = False
while not flag:
    while ryad + 1 < m and lst[stroka][ryad + 1] == ".":
        count += 1
        lst[stroka][ryad + 1] = count
        ryad += 1
    while stroka + 1 < n and lst[stroka + 1][ryad] == ".":
        count += 1
        lst[stroka + 1][ryad] = count
        stroka += 1
    while ryad - 1 > -1 and lst[stroka][ryad - 1] == ".":
        count += 1
        lst[stroka][ryad - 1] = count
        ryad -= 1
    while stroka - 1 > -1 and lst[stroka - 1][ryad] == ".":
        count += 1
        lst[stroka - 1][ryad] = count
        stroka -= 1
    flag = proverka(lst)
for a in lst:
    print(*a)


