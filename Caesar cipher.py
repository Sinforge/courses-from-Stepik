text = input()
text = text.split(' ')
result = ""
for m in text:
    m = str(m)
    l = 0
    for j in m:
        if j in alpha1 or j in alpha2:
            l += 1

    for i in m:
        if i.isalpha():
            if i.islower() == True:
                k = ord(i) + l
                if k > 122:
                    k = k - 122 + 96
                    result += chr(k)
                else:
                    result += chr(k)
            else:
                k = ord(i) + l
                if k > 90:
                    k = k - 90 + 64
                    result += chr(k)
                else:
                    result += chr(k)
        else:
            result += i
    result += " "
